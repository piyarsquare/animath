#!/usr/bin/env node
/* Regenerate docs/sessions/index.html — the cross-session dashboard — from the
   <script class="report-meta"> JSON island embedded in every session report.
   No dependencies. Run from anywhere:  node docs/sessions/build-index.mjs

   Generating (rather than fetching at view time) keeps index.html a static file
   that works over file:// too. The /handoff skill runs this after writing a
   handoff; you can also run it by hand any time reports are added or edited. */
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join, dirname, relative } from "node:path";
import { fileURLToPath } from "node:url";

const sessionsDir = dirname(fileURLToPath(import.meta.url));
const META_RE = /<script[^>]*class="report-meta"[^>]*>([\s\S]*?)<\/script>/i;

function walk(dir) {
  let out = [];
  let entries;
  try { entries = readdirSync(dir, { withFileTypes: true }); } catch { return out; }
  for (const e of entries) {
    const p = join(dir, e.name);
    if (e.isDirectory()) out = out.concat(walk(p));
    else if (e.isFile() && e.name.endsWith(".html") && !e.name.startsWith("_") && e.name !== "index.html") out.push(p);
  }
  return out;
}

const esc = (s) => String(s == null ? "" : s).replace(/[&<>"]/g, (c) => ({ "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;" }[c]));

const reports = [];
for (const kind of ["progress", "handoff"]) {
  for (const file of walk(join(sessionsDir, kind))) {
    const m = readFileSync(file, "utf8").match(META_RE);
    if (!m) { console.warn("• no report-meta:", relative(sessionsDir, file)); continue; }
    let meta;
    try { meta = JSON.parse(m[1]); } catch (err) { console.warn("• bad report-meta JSON:", relative(sessionsDir, file), "—", err.message); continue; }
    meta._href = relative(sessionsDir, file).split(/[\\/]/).join("/");
    reports.push(meta);
  }
}

// Group by branch slug → session id → { progress, handoff }
const groups = new Map();
for (const r of reports) {
  const slug = r.slug || "unknown";
  if (!groups.has(slug)) groups.set(slug, { branch: r.branch || slug, sessions: new Map() });
  const g = groups.get(slug);
  if (r.branch) g.branch = r.branch;
  if (!g.sessions.has(r.session)) g.sessions.set(r.session, {});
  g.sessions.get(r.session)[r.kind] = r;
}

const statusBadge = (s) => {
  const map = { completed: "badge-ok", "in-progress": "badge-warn", "design-only": "badge", "investigation-only": "badge" };
  return `<span class="badge ${map[s] || "badge"}">${esc(s || "unknown")}</span>`;
};
const buildBadge = (b) => b ? `<span class="badge ${b === "passed" ? "badge-ok" : b === "failed" ? "badge-bad" : "badge-warn"}">build: ${esc(b)}</span>` : "";
const followupBadge = (f) => {
  if (!f) return "";
  const cls = /^(low|none)$/i.test(f) ? "badge-ok" : /^medium$/i.test(f) ? "badge-warn" : "badge-bad";
  return `<span class="badge ${cls}">follow-up: ${esc(f)}</span>`;
};

// latest session id per group, for ordering (descending)
const latest = (g) => [...g.sessions.keys()].sort().slice(-1)[0] || "";
const sortedGroups = [...groups.entries()].sort((a, b) => latest(b[1]).localeCompare(latest(a[1])));

let cards = "";
let nSessions = 0;
for (const [slug, g] of sortedGroups) {
  const sids = [...g.sessions.keys()].sort().reverse();
  let inner = "";
  for (const sid of sids) {
    nSessions++;
    const s = g.sessions.get(sid);
    const primary = s.handoff || s.progress;            // prefer the handoff's metadata
    const links = [];
    if (s.progress) links.push(`<a href="${esc(s.progress._href)}">progress</a>`);
    if (s.handoff) links.push(`<a href="${esc(s.handoff._href)}">handoff</a>`);
    const pr = primary.pr && primary.pr.num
      ? `<a href="${esc(primary.pr.url)}">PR #${esc(primary.pr.num)}</a>` : "";
    inner += `
      <div class="session-card">
        <div class="row">
          <span class="sid">${esc(sid)}</span>
          <span class="title">${esc(primary.title || "")}</span>
          ${statusBadge(primary.status)}
          ${buildBadge(primary.build)}
          ${followupBadge(primary.followup)}
        </div>
        <div class="links">${links.join(" · ")}${pr ? " · " + pr : ""}</div>
      </div>`;
  }
  cards += `
    <section class="branch-group">
      <h2><code>${esc(g.branch)}</code></h2>
      ${inner}
    </section>`;
}

const now = new Date().toISOString().slice(0, 10);
const html = `<!DOCTYPE html>
<!-- GENERATED by build-index.mjs — do not edit by hand; re-run the script. -->
<html lang="en">
<head>
  <meta charset="utf-8">
  <meta name="viewport" content="width=device-width, initial-scale=1">
  <title>animath · session reports</title>
  <link rel="stylesheet" href="./report.css">
  <script defer src="./report.js"></script>
</head>
<body>
<main class="report">
  <header>
    <p class="kicker">animath</p>
    <h1>Session reports</h1>
    <dl class="meta">
      <div><dt>Branches</dt><dd>${groups.size}</dd></div>
      <div><dt>Sessions</dt><dd>${nSessions}</dd></div>
      <div><dt>Generated</dt><dd>${now} · <code>build-index.mjs</code></dd></div>
    </dl>
    <p class="lineage">Open a report locally in a browser for the full styling
    (GitHub shows <code>.html</code> as source). Progress = live session log;
    handoff = the distilled summary.</p>
  </header>
  <div class="body">
    <div class="content">
      ${cards || "<p>No session reports yet.</p>"}
    </div>
  </div>
</main>
</body>
</html>
`;

writeFileSync(join(sessionsDir, "index.html"), html);
console.log(`index.html: ${nSessions} session(s) across ${groups.size} branch(es).`);
